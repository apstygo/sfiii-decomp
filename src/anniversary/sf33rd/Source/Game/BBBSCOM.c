void makeup_bonus_game_level(s16 ix) {
    s16 emid = (ix + 1) & 1;
    u16 swdat;

    if (emid) {
        swdat = p2sw_0 ;
    } else {
        swdat = p1sw_0;
    }

    bbbs_type = 1;

    if (katteni_bonus_nando(swdat)) {
        Bonus_Stage_Level = set_bonus_game_nando(swdat);

        if (Bonus_Stage_Level > 4) {
            bbbs_type = 0;
            Bonus_Stage_Level -= 5;
        }
    } else {
        Bonus_Stage_Level = set_bonus_game_difficulty(emid);
    }
}


// INCLUDE_ASM("asm/anniversary/nonmatchings/sf33rd/Source/Game/BBBSCOM", set_bonus_game_difficulty);

s32 set_bonus_game_difficulty(s16 emid) {
    s16 grade = judge_final[emid][0].vs_cpu_grade[11];

    if (grade < 9) {
        return 0;
    } else if (grade < 15) {
        return 1;
    } else if (grade < 18) {
        return 2;
    } else if (grade < 21) {
        return 3;
    } else {
        return 4;
    }
}

// INCLUDE_ASM("asm/anniversary/nonmatchings/sf33rd/Source/Game/BBBSCOM", set_bonus_game_nando);

s32 set_bonus_game_nando(u16 swdat) {
    if (swdat == 0x252) {
        return 9;
    }
    if (swdat == 0x702) {
        return 8;
    }
    if (swdat == 0x402) {
        return 7;
    }
    if (swdat == 0x202) {
        return 6;
    }
    if (swdat == 0x102) {
        return 5;
    }
    if (swdat == 0x521) {
        return 4;
    }
    if (swdat == 0x71) {
        return 3;
    }
    if (swdat == 0x41) {
        return 2;
    }
    if (swdat == 0x21) {
        return 1;
    }
    return 0;
}

// INCLUDE_ASM("asm/anniversary/nonmatchings/sf33rd/Source/Game/BBBSCOM", katteni_bonus_nando);

s32 katteni_bonus_nando(u16 swdat) {
    if ((swdat & 1) && (swdat & 0x70)) {
        return 1;
    }
    if ((swdat & 2) && (swdat & 0x700)) {
        return 1;
    }
    return 0;
}

